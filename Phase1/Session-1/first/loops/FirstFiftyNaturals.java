package com.bhavitha.first.loops;

public class FirstFiftyNaturals {
	public static void main(String[] args) {
		
		// printing first 50 natural numbers using whilw loop
		
		int i=1;
		while(i<=50) {
			System.out.println(i);
			i++;
		}
		
		System.out.println();
		// using for loop
		
		for(int j=1;j<51;j++)
			System.out.println(j);
	}

}
